'instance': What you get when you tell Ruby to create a class

'def': How you define a function

'@': Access the instance var within current scope. class/fn/module

'inheritance': The concept that a class can be composed of other classes as parts, muych liek how a car has wheels

'attribute': A property classes have that are from composition and are iusually variables.

'is-a': Phrase to say that something inherits from another, as in a "salmon" is-a "fish"

'has-a': Phraseto say that something si composed of other thingfs or has a trail, as in "a salmon has-a mouth"

